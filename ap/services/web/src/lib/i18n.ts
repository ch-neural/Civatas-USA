/** Reactive hook that returns a function for translating raw persona-data values
 *  (Chinese → localized). Falls through to the original value if no mapping exists. */
export function useLocalizePersonaValue(): (raw: string | undefined | null) => string {
  const t = useTr();
  return (raw) => {
    if (!raw) return raw || "";
    const key = PERSONA_VALUE_KEY[raw];
    return key ? t(key) : raw;
  };
}

/** Variant that uses an override locale (e.g. force English for a US template). */
export function useLocalizePersonaValueWithLocale(override?: Locale | null): (raw: string | undefined | null) => string {
  const t = useTrWithLocale(override);
  return (raw) => {
    if (!raw) return raw || "";
    const key = PERSONA_VALUE_KEY[raw];
    return key ? t(key) : raw;
  };
}

