// ── Public API ─────────────────────────────────────────────────────

export type StringKey = keyof typeof STRINGS;

/** Replace `{name}` placeholders with values from `vars`. */
function formatStr(template: string, vars?: Record<string, string | number>): string {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, name) => {
    const v = vars[name];
    return v === undefined || v === null ? `{${name}}` : String(v);
  });
}

/** Lookup a translation by key + locale, with English fallback. */
export function tr(key: StringKey, locale: Locale, vars?: Record<string, string | number>): string {
  const entry = STRINGS[key];
  if (!entry) return key;
  const raw = entry[locale] ?? entry.en ?? key;
  return formatStr(raw, vars);
}

/** Reactive hook — re-renders subscribers when locale changes. */
export function useTr(): (key: StringKey, vars?: Record<string, string | number>) => string {
  const { locale } = useLocaleStore();
  return (key, vars) => tr(key, locale, vars);
}

/**
 * Variant of useTr that accepts an override locale. Used by panels that want
 * simulation-result strings (chart headers, persona data values) to follow
 * the active TEMPLATE's locale instead of the user's UI locale toggle.
 *
 * Example: a synthesis chart for a US presidential template should always
 * render personality dimension labels in English, even if the user has
 * toggled the UI to Traditional Chinese.
 */
export function useTrWithLocale(override?: Locale | null): (key: StringKey, vars?: Record<string, string | number>) => string {
  const { locale } = useLocaleStore();
  const effective: Locale = override || locale;
  return (key, vars) => tr(key, effective, vars);
}

