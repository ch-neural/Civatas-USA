/**
 * Civatas-USA i18n core (Stage 1.7+).
 *
 * Lightweight string dictionary + reactive `useTr()` hook backed by the
 * locale-store. Replaces the inline `isEn ? "EN" : "中文"` pattern that
 * Stage 1.5/1.6 used while sweeping the panels.
 *
 * Adding a new translatable string:
 *   1. Add a key + entry to STRINGS below
 *   2. Use `t("your.key")` in the component
 *
 * Adding a new language:
 *   1. Extend the UiLocale type in store/locale-store.ts
 *   2. Add a third (or fourth …) field to every entry in STRINGS
 *
 * Variable interpolation:
 *   t("synthesis.summary.tagline", { count: personas.length })
 *   …where the dict entry is "{count} synthetic agents".
 *
 * Falls back to the English string if the requested locale is missing,
 * and to the key itself if both are missing — that way an unknown key
 * is visible in the UI as a debugging hint instead of crashing.
 */
